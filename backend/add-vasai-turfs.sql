    COUNT(CASE WHEN location_city = 'Virar' THEN 1 END) as virar_turfs,
    COUNT(CASE WHEN location_city = 'Nalasopara' THEN 1 END) as nalasopara_turfs,
    COUNT(CASE WHEN location_city = 'Bhayandar' THEN 1 END) as bhayandar_turfs,
    COUNT(CASE WHEN location_city = 'Mira Road' THEN 1 END) as mira_road_turfs,
    COUNT(CASE WHEN location_city = 'Naigaon' THEN 1 END) as naigaon_turfs,
    COUNT(CASE WHEN location_city = 'Kandivali' THEN 1 END) as kandivali_turfs,
    COUNT(CASE WHEN location_city = 'Borivali' THEN 1 END) as borivali_turfs
FROM turfs 
WHERE location_city IN ('Vasai', 'Virar', 'Nalasopara', 'Bhayandar', 'Mira Road', 'Naigaon', 'Kandivali', 'Borivali'); 